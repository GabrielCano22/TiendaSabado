contenedorzoro.addEventListener("click",function (event){

     if(event.target.classList.contains("btn")){
          console.log("Estoy haciendo clic en el boton")
          let modalzoro = new bootstrap.Modal(document.getElementById('modalzoro'))
          modalzoro.show()
      }

})

